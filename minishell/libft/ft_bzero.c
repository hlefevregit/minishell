#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*dest;
	int		i;

	i = 0;
	dest = (char *)s;
	while (n > 0)
	{
		dest[i] = '\0';
		i++;
		n--;
	}
}
